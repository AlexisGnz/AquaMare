        <h3 class="header-stick-accesorios">CLIMATIZACION SOLAR DE PISCINAS: <strong>Colector Solar</strong></h3>
        <section>
         <p>El colector solar está fabricado de polipropileno de alta densidad, un material muy durable, 
y solo pesa 5 Kg. por m2 con carga de agua. Opera eficientemente en todos su ángulos.</p>
        <p>Usando la bomba existente, el agua de la piscina es dirigida luego de pasar por el filtro 
a través de una serie de válvulas a los colectores solares. El agua entra por abajo y sube por los 
tubos individuales del colector; que al pasar por este es calentada por la energía radiante del 
sol y retorna a la piscina para repetir el ciclo hasta que la misma haya sido calentada.</p>
        <a href="">Aquamare ofrece Garantía escrita por 10 años. Solución sustentable. Este sistema no daña el medio ambiente.</a>
        <strong>Ventajas: Alta duración contra la corrosión, cal, cloro, yodo, etc. Vida útil de más de 20 años. Sistema liviano.
Resistente a los rayos UV. No acumula humedad. No requiere mantenimiento. Sin consumo de gas.</strong>
